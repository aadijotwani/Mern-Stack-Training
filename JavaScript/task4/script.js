function changeColorBlock(color) {
  const colorBlock = document.getElementById("outerBlock");
  colorBlock.style.backgroundColor = color;
}


function changeColorHeading(color)
{
    const colorBlock = document.getElementById("heading");
    colorBlock.style.color = color;
}

function changeColorParagraph(color)
{
  const colorBlock = document.getElementById("paragraph")
  colorBlock.style.color = color;
}
